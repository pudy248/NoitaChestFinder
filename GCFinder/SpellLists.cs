    static Spell GetRandomAction(uint seed, bool[] unlockedSpells, float x, float y, int level, int offset = 0)
	{
		NoitaRandom prng = new NoitaRandom(seed + (uint)offset);
		prng.SetRandomSeed(x, y);

		double sum = 0;
		// all_spells length is 393
		for (int i = 0; i < 393; i++)
		{
			if (!unlockedSpells[i])
			{
				continue;
			}
			sum += all_spells[i].spawn_probabilities[level];
		}

		double multiplier = prng.Next();
		double accumulated = sum * multiplier;

		for (int i = 0; i < 393; i++)
		{
			if (!unlockedSpells[i])
			{
				continue;
			}
			Spell spell = all_spells[i];
			double probability = spell.spawn_probabilities[level];
			if (probability == 0)
			{
				continue;
			}
			if (probability >= accumulated)
			{
				return spell;
			}
			accumulated -= probability;
		}

		return all_spells[0]; // Fallback just in case. Should be mathematically impossible
	}

